export type EventKind = "trigger" | "domain" | "error" | "gate";

export type EventDef = {
  name: string;
  stage: string;
  kind: EventKind;
  rate: number;
  err: number;
  desc: string;
  publishers: string[];
  subscribers: string[];
  wf: string[];
  emits: string[];
  data: [string, string][];
  mutations: string[];
  selected?: boolean;
  // Provenance — added when /api/events serves the row. 'neo4j' is canonical;
  // 'hardcoded' means cold-start fallback (this file). UI badges accordingly.
  source?: 'neo4j' | 'hardcoded' | 'manual';
  syncedAt?: string | null;
  activeVersions?: string[];
  // Validator provenance: where the Zod schema actually came from at runtime.
  // Different from `source` (metadata): a row can have Neo4j metadata but
  // builtin validator if Neo4j's JSON Schema couldn't be converted.
  schemaSource?: 'neo4j' | 'builtin' | 'missing';
  versionSources?: Array<{ version: string; source: 'neo4j' | 'builtin'; fallbackReason?: string }>;
  // Raw RAAS field / mutation / source-action lists (only when source==='neo4j').
  // Powers the Payload + Subscribers tabs with full fidelity. The legacy
  // `data: [name, type][]` and `mutations: string[]` shapes above are kept
  // for hardcoded fallback events; new code reads these instead.
  fields?: Array<{ name: string; type: string; required: boolean; position: number; targetObject: string | null }>;
  mutationsV2?: Array<{ targetObject: string; mutationType: string; impactedProperties: string[] }>;
  sourceAction?: string | null;
  /** JSON Schema generated by the sync worker; rendered by Payload tab "JSON Schema" view. */
  schema?: object | null;
  /** Allmeta breaking-change flag — UI badges in red. */
  isBreakingChange?: boolean;
  /** Last time sync detected a content delta (vs. just touching the row). */
  lastChangedAt?: string | null;
  /** Set when sync ran and Allmeta no longer ships this event. UI shows "已下架". */
  retiredAt?: string | null;
  /** Allmeta source file (e.g. events_v0_1_035.json) — for traceability. */
  sourceFile?: string | null;
};

