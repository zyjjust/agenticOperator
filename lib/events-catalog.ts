export function kindDot(kind: EventKind): string {
  switch (kind) {
    case "error": return "var(--c-err)";
    case "gate": return "oklch(0.5 0.14 75)";
    case "trigger": return "var(--c-info)";
    default: return "var(--c-ok)";
  }
}
